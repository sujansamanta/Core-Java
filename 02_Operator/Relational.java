
public class Relational {
    public static void main(String[] args) {
        int p = 20 , q =15;
        System.out.println(p==q);
        System.out.println(p!=q);
        System.out.println(p>=q);
        System.out.println(p<=q);
        System.out.println(p>q);
        System.out.println(p<q);
    }
    
    
}
