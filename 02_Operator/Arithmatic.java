
public class Arithmatic {
    public static void main(String[] args) {
        int p = 20 , q =3;
        System.out.println(p+q);
        System.out.println(p-q);
        System.out.println(p*q);
        System.out.println(p/q);
        System.out.println(p%q);
        System.out.println(++p);
    }
    
}
